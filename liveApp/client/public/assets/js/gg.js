function goToEmployees() {
  const loginTime = localStorage.getItem("loginTime");
  const now = Date.now();
  
  if (loginTime) {
    const elapsed = now - loginTime;
    const thirtyMinutes = 30 * 60 * 1000;

    if (elapsed <= thirtyMinutes) {
      // User is logged in, proceed to employees page
      window.location.href = "Our Employees DataBase.html"; 
    } else {
      // Session expired
      alert("Session expired. Please log in again.");
      window.location.href = "admin.html"; // Redirect to login page
    }
  } else {
    // No login time found, redirect to login
    window.location.href = "admin.html"; // Redirect to login page
  }
}


// Check if the user is logged in
function checkLogin() {
  const loginTime = localStorage.getItem("loginTime");
  const now = Date.now();

  if (loginTime) {
    const elapsed = now - loginTime;
    const thirtyMinutes = 30 * 60 * 1000;

    if (elapsed > thirtyMinutes) {
      // Clear the login time if 30 minutes have passed
      localStorage.removeItem("loginTime");
      alert("Session expired. Please log in again.");
      window.location.href = "admin.html"; // Redirect to login page
    }
  }
}

